// Función para obtener un texto traducido
function t(key) {
  const lang = localStorage.getItem('mardehilo-lang') || 'es';
  return translations[lang]?.[key] || key;
}

// Aplicar traducciones a todos los elementos con data-i18n
function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = text;
    } else if (el.tagName === 'IMG') {
      el.alt = text;
    } else {
      el.textContent = text;
    }
  });
}

// Escuchar cambios de idioma
document.addEventListener('languageChanged', function(e) {
  applyTranslations();
  // Actualizar atributo lang del html
  document.documentElement.lang = e.detail.lang;
});

// Aplicar al cargar
document.addEventListener('DOMContentLoaded', applyTranslations);

// Exponer t globalmente para uso en otros scripts
window.t = t;
